<?php

namespace Dmuggle\Calculator;

class Calc {
    public static function add($a, $b){
        return $a + $b;
    }

    public static function substract($a, $b){
        return $a - $b;
    }
    
    public static function divide($a, $b){
        return $a / $b;
    }

    public static function multiply($a, $b){
        return $a * $b;
    }
}